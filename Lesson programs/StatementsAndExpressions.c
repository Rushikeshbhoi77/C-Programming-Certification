#include <stdio.h>

int main() {
    //Declaration statement
    int a, b, sum;

    //Asignment statement
    a = 10;
    b = 20;

    //Expression statement
    sum = a + b;

    //Function call statement
    printf("Sum = %d\n", sum);

    //Control statement
    if (sum > 15) {
        printf("Sum is greater than 15\n");
    }
        //Return Statement
        return 0;
}
